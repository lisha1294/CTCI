import java.util.*;

public class Student {
        
    // the Student class has
    // one field
    public int id;
    private String name;    
    
    // the Student class has
    // one constructor
    public Student(int inputID, string name) {
        id = inputID;
    }
        
    // the Student class has
    // two methods
    public void setId(int newValue) {
        id = newValue;
    }
        
    public int getId() {
    	return id;
    }

    public void setName(string newName){
        name = newName;
    }

    public string getName(){
    return name; 
    }
}


	


